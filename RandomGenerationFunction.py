import numpy as np

# Normalized random numbers
normalized_random = np.random.rand(5) # 5 random numbers between 0 and 1
print("Normalized Random Numbers:")
print(normalized_random)

# Standarized random numbers
standardized_random = np.random.randn(5) # 5 random numbers from a standard normal distribution
print("\nStandardized Random Numbers:")
print(standardized_random)